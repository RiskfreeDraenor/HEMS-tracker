// ============================================================================
//  populate-airports.mjs
//
//  One-time (and safe-to-re-run) script that fills the D1 `airports` table
//  with US airports + heliports from the OurAirports public-domain dataset.
//
//  Usage:
//     node populate-airports.mjs
//
//  Requires Node 18+ (uses global fetch) and wrangler in PATH.
//
//  Produces ./airports.sql, then runs `wrangler d1 execute hemstracker-logs`
//  to import it into the remote database.
// ============================================================================

import { writeFileSync, existsSync } from "node:fs";
import { execSync } from "node:child_process";

const CSV_URL  = "https://davidmegginson.github.io/ourairports-data/airports.csv";
const KEEP_TYPES = new Set(["small_airport","medium_airport","large_airport","heliport","seaplane_base"]);
const KEEP_COUNTRY = "US";   // expand here if you ever track HEMS outside the US
const BATCH_SIZE = 200;      // rows per INSERT statement

// ---------------------------------------------------------------------------
console.log("Fetching OurAirports CSV…");
const res = await fetch(CSV_URL);
if (!res.ok) { console.error(`Failed to fetch: HTTP ${res.status}`); process.exit(1); }
const text = await res.text();
console.log(`Got ${(text.length / 1024 / 1024).toFixed(1)} MB`);

// ---------------------------------------------------------------------------
function parseCsvLine(line) {
  // Minimal RFC 4180 parser: handles quoted fields with embedded commas.
  const out = [];
  let i = 0, cur = "", inQ = false;
  while (i < line.length) {
    const c = line[i];
    if (inQ) {
      if (c === '"' && line[i+1] === '"') { cur += '"'; i += 2; continue; }
      if (c === '"') { inQ = false; i++; continue; }
      cur += c; i++;
    } else {
      if (c === ',') { out.push(cur); cur = ""; i++; continue; }
      if (c === '"') { inQ = true; i++; continue; }
      cur += c; i++;
    }
  }
  out.push(cur);
  return out;
}

const lines  = text.split(/\r?\n/);
const header = parseCsvLine(lines[0]);
const col    = name => header.indexOf(name);
const idx = {
  ident:        col("ident"),
  type:         col("type"),
  name:         col("name"),
  lat:          col("latitude_deg"),
  lon:          col("longitude_deg"),
  country:      col("iso_country"),
  municipality: col("municipality"),
  iata:         col("iata_code"),
};
if (Object.values(idx).some(v => v < 0)) { console.error("Unexpected CSV header:", header); process.exit(1); }

// ---------------------------------------------------------------------------
const airports = [];
for (let i = 1; i < lines.length; i++) {
  const ln = lines[i]; if (!ln) continue;
  const f  = parseCsvLine(ln);
  const type = f[idx.type];
  const country = f[idx.country];
  if (!KEEP_TYPES.has(type)) continue;
  if (country !== KEEP_COUNTRY) continue;
  const lat = parseFloat(f[idx.lat]);
  const lon = parseFloat(f[idx.lon]);
  if (!Number.isFinite(lat) || !Number.isFinite(lon)) continue;
  airports.push({
    ident:        f[idx.ident],
    iata:         f[idx.iata] || null,
    name:         f[idx.name],
    type,
    lat, lon,
    country,
    municipality: f[idx.municipality] || null,
  });
}
console.log(`Filtered to ${airports.length} ${KEEP_COUNTRY} airports/heliports`);

// ---------------------------------------------------------------------------
function sqlString(v) {
  if (v == null) return "NULL";
  return "'" + String(v).replace(/'/g, "''") + "'";
}

function chunked(arr, size) {
  const out = [];
  for (let i = 0; i < arr.length; i += size) out.push(arr.slice(i, i + size));
  return out;
}

const sqlParts = [
  "-- Generated by populate-airports.mjs",
  "-- Safe to re-run: this clears the table and reloads from OurAirports.",
  "DELETE FROM airports;",
];
for (const batch of chunked(airports, BATCH_SIZE)) {
  const values = batch.map(a =>
    `(${sqlString(a.ident)}, ${sqlString(a.iata)}, ${sqlString(a.name)}, ${sqlString(a.type)}, ${a.lat}, ${a.lon}, ${sqlString(a.country)}, ${sqlString(a.municipality)})`
  ).join(",\n  ");
  sqlParts.push(`INSERT OR REPLACE INTO airports (ident, iata, name, type, lat, lon, country, municipality) VALUES\n  ${values};`);
}
const sql = sqlParts.join("\n\n");
writeFileSync("airports.sql", sql);
console.log(`Wrote airports.sql (${(sql.length / 1024).toFixed(1)} KB)`);

// ---------------------------------------------------------------------------
console.log("Importing into D1 (remote)…");
try {
  execSync(`wrangler d1 execute hemstracker-logs --file=airports.sql --remote --yes`, { stdio: "inherit" });
  console.log("Done. The `airports` table in D1 is now populated.");
} catch (e) {
  console.error("wrangler d1 execute failed. Make sure wrangler is logged in and the worker is already deployed.");
  process.exit(1);
}
